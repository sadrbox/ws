// ─────────────────────────────────────────────────────────────────────────────
// Поисковое слово → условие по `id`, если это вообще похоже на id.
//
// Зачем отдельный хелпер: `id` в БД — int4. Пользователь же вводит в поиск ДЛИННЫЕ
// числа — EAN-13 («4650123456789»), БИН/ИИН (12 цифр), номер счёта. Прямое
// `{ id: { equals: Number(w) } }` уходило в Postgres и падало там с
// «22003 value out of range for type integer» — весь список отвечал 500 вместо
// того, чтобы просто поискать это число как ТЕКСТ (штрихкод, БИН).
//
// Поэтому по id ищем только когда число в него влезает.
// ─────────────────────────────────────────────────────────────────────────────
const INT4_MAX = 2147483647;

/** @returns {object|null} `{ id: { equals } }` либо null — слово не годится в id. */
export function idSearchCondition(word) {
	const num = Number(word);
	if (!Number.isInteger(num) || num <= 0 || num > INT4_MAX) return null;
	return { id: { equals: num } };
}

export default { idSearchCondition };
