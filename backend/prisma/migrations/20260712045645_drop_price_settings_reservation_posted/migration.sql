-- ── 1. Удаление таблиц-сирот «Установка цен» ────────────────────────────────
-- Корректировка цен — это ОБРАБОТКА (групповое изменение данных), а не документ:
-- она пишет напрямую в product_prices. Документ ProductPriceSetting был
-- спроектирован (шапка с posted + строки), но роутер к нему так и не написали:
-- таблицы остались пустыми (0 строк), а фронт всё это время работал с product-prices.
-- Из-за них в списке рендерились фантомные колонки («Проведён»/«Номер»/«Организация»).
DROP TABLE IF EXISTS "product_price_setting_items";
DROP TABLE IF EXISTS "product_price_settings";

-- ── 2. Бэкфилл: существующие резервы считаем проведёнными ───────────────────
-- Регистр резервов теперь движет только ПРОВЕДЁННЫЙ резерв (раньше проверки
-- posted не было — черновик молча уменьшал доступный остаток). Все существующие
-- резервы фактически резервировали товар, поэтому помечаем их проведёнными:
-- иначе они разом перестали бы резервировать, а строки регистра «повисли» бы.
UPDATE "reservations" SET "posted" = true WHERE "deletedAt" IS NULL AND "posted" = false;
