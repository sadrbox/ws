-- Параметры учёта: прошлые версии — АРХИВ, а не удалённые записи.
--
-- Настройки версионируются (каждое сохранение = новая запись со своим startDate), и от
-- версии зависят уже проведённые документы: метод себестоимости и признак НДС берутся
-- на ДАТУ ДОКУМЕНТА. Но сохранение помечало прошлые версии deletedAt, а расчёт фильтровал
-- deletedAt IS NULL — история вычищалась из выборки. В итоге на дату документа не
-- находилось ни одной версии и подставлялся дефолт: прошлые периоды пересчитывались по
-- НОВЫМ правилам. Ровно то, что версионирование должно было предотвратить.
--
-- Снимаем пометку: эти записи никто не удалял, их «удалил» механизм версионирования.
-- Дальше deletedAt ставится только настоящим удалением (DELETE), а «действующей»
-- считается последняя версия по (startDate, id) — см. services/accountingSettings.js.
UPDATE "organization_accounting_settings" SET "deletedAt" = NULL WHERE "deletedAt" IS NOT NULL;
